int	ft_atoi(char *str)
{
	int	i;
	int	minus;
	int	ret;

	ret = 0;
	minus = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			minus++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		ret *= 10;
		ret += str[i] - '0';
		i++;
	}
	if (minus % 2 == 1)
		ret = -ret;
	return (ret);
}
