#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long	b;

	b = (long)nb;
	if (b < 0)
	{
		b = b * (-1);
		write(1, "-", 1);
	}
	if (b >= 10)
	{
		ft_putnbr(b / 10);
		b = b % 10;
	}
	if (b < 10)
		ft_putchar(b + '0');
}
