#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		a;
	int		i;
	char	*b;

	a = argc - 1;
	while (a > 0)
	{
		b = argv[a];
		i = 0;
		while (b[i] != '\0')
		{
			write(1, &b[i], 1);
			i++;
		}
		write(1, "\n", 1);
		a--;
	}
	return (0);
}
