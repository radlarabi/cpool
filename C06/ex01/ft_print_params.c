#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		a;
	char	*b;
	int		i;

	a = 1;
	while (a < argc)
	{
		b = argv[a];
		i = 0;
		while (b[i] != '\0')
		{
			write(1, &b[i], 1);
			i++;
		}
		write(1, "\n", 1);
		a++;
	}	
	return (0);
}
